import assert from 'node:assert/strict'
import { readFileSync } from 'node:fs'

const scoringSource = readFileSync(new URL('../src/lib/scoring.ts', import.meta.url), 'utf8')
const poolViewSource = readFileSync(new URL('../src/app/(app)/pool/[id]/PoolView.tsx', import.meta.url), 'utf8')
const poolPageSource = readFileSync(new URL('../src/app/(app)/pool/[id]/page.tsx', import.meta.url), 'utf8')
const landingSource = readFileSync(new URL('../src/app/page.tsx', import.meta.url), 'utf8')
const layoutSource = readFileSync(new URL('../src/app/layout.tsx', import.meta.url), 'utf8')

assert.match(scoringSource, /status !== 'active'/, 'OB stand-ins should include cut, WD, DNQ, and missing/non-active picks')
assert.doesNotMatch(scoringSource, /status === 'cut'/, 'OB stand-ins should not be limited to missed-cut players')
assert.match(poolViewSource, /const visibleEntries = activeEntries/, 'PoolView should show every active entry on the leaderboard before lock/start')
assert.match(poolViewSource, /picks_hidden/, 'PoolView should mask other entrants picks before lock/start')
assert.match(poolPageSource, /const picksAreVisible = pool\.is_locked \|\| scoringIsLive/, 'Server pool page should only reveal picks after lock/start')
assert.match(poolPageSource, /golfer_picks: \[\]/, 'Server pool page should mask other entrants picks before lock/start while keeping entries visible')
assert.match(poolViewSource, /buildPreScoringEntry/, 'PoolView should build pre-scoring own-picks rows')
assert.match(poolViewSource, /localeCompare/, 'Pre-scoring picks should be alphabetical before top-counting display')
assert.doesNotMatch(landingSource, /-mb-10|-mb-12|-mt-10|-mt-12/, 'Hero boundary should not overlap the next section; it should end at the bottom of the post')
assert.match(landingSource, /<section className="border-y border/, 'Next green section should start normally at the hero boundary')
assert.doesNotMatch(landingSource, />Rank<\/th>/, 'Desktop demo should not show the clipped Rank header label')
assert.match(landingSource, /name: 'Lonnie'/, 'Landing demo should include Lonnie entry')
assert.match(landingSource, /name: 'Jeff'/, 'Landing demo should include Jeff entry')
assert.match(landingSource, /name: 'Dan M'/, 'Landing demo should include Dan M entry')
assert.match(landingSource, /Tiger&apos;s Tribe - PGA Championship pool/, 'Landing demo pool name should be Tiger\'s Tribe')
assert.match(landingSource, /gpp-board-frame/, 'Landing demo board frame should use the richer clubhouse board treatment')
assert.match(poolViewSource, /gpp-board-frame/, 'Real leaderboard board frame should use the richer clubhouse board treatment')
assert.match(landingSource, /gpp-board-post/, 'Landing demo post should use the dimensional structural post treatment')
assert.match(poolViewSource, /gpp-board-post/, 'Real leaderboard post should use the dimensional structural post treatment')
assert.match(layoutSource, /favicon\.svg\?v=3/, 'Favicon should prefer the cache-busted SVG GPP tab icon')

console.log('scoring/visibility/landing/favicon rule markers present')
